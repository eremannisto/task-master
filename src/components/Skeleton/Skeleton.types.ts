/**
 * Skeleton component props
 * - width    : Width of the skeleton
 * - height   : Height of the skeleton
 * - radius   : Border radius of the skeleton
 * - className: Additional CSS class name
 */
export interface SkeletonComponent {
  width?     : string | number;
  height?    : string | number;
  radius?    : string | number;
  className? : string;
}